
package modelo;


public class Usuario {
 
    private String usuario;
    private int nivel;

    public Usuario() {
    }

    public Usuario(String usuario, int nivel) {
        this.usuario = usuario;
        this.nivel = nivel;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    
    
    
    
    
    
}
